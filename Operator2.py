#Membership Operator
musik = ["Jazz", "Hip-hop", "Rock"]
print("Jazz" in musik)
print("Classic" not in musik)

Title = "Fallout New Vegas"

print("F" in Title)
print("fallout" in Title)
print("b" not in Title)

#Bitwise operator
print(7 & 4)
print(7 | 4)
print(7 ^ 4)

#Precedence Operator
print((7 + 4) - (7 + 4))
print(200 + 6 * 4)
print(6 + 5 - 8 * 4)